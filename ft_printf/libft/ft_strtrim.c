#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*str;
	size_t	l_str;
	size_t	start;
	size_t	end;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	end = ft_strlen(s1);
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	if (start >= end)
		return (ft_strdup(""));
	while ((end > start) && ft_strchr(set, s1[end - 1]))
		end--;
	l_str = end - start;
	str = (char *)malloc(l_str + 1);
	if (!str)
		return (NULL);
	ft_strlcpy(str, &s1[start], l_str + 1);
	return (str);
}
